# Minimizer-Based Sketching: ENCODE Validation Experiments

## Project Summary

This project validates minimizer-based sequence sketching using publicly available ENCODE and Roadmap Epigenomics ChIP-seq data through two complementary experiments:

**Experiment A — Cross-Reference Robustness**  
The same biological sample aligned to GRCh37 and GRCh38 should produce more similar sketches than sketches from a different biological sample aligned to either reference.

**Experiment B — Tissue-over-Species Clustering**  
Minimizer sketch similarity should reproduce the well-established result that epigenomic profiles cluster by tissue type rather than by species when comparing matched human and mouse samples. This recapitulates findings from Yue et al. (2014), Lin et al. (2014), and the Roadmap Epigenomics Consortium (2015).

